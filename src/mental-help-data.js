/**
 * @param {Record<string, string>} answers
 * @param {"male" | "female" | null} gender
 * @param {typeof MH_STEPS} [steps]
 */
export function buildWordBlockBody(answers, gender, steps = MH_STEPS) {
  /** @type {Record<MhWordKey, string[]>} */
  const chunks = { complaints: [], disease: [], life: [] };
  steps.forEach((step) => {
    if (step.id === "complaints") {
      const state = parseComplaintsString(answers.complaints);
      const line = formatComplaintsForWord(state, gender).trim();
      if (line && line !== "Жалобы на: —") chunks[step.wordKey].push(line);
    } else if (step.id === "life-structured") {
      const state = parseLifeStructuredString(answers[step.id]);
      const line = formatLifeStructuredForWord(state, gender).trim();
      if (line) chunks[step.wordKey].push(line);
    } else if (step.id === DISEASE_STRUCTURED_ID) {
      const state = parseDiseaseStructuredString(answers[step.id]);
      const line = formatDiseaseStructuredForWord(state, gender).trim();
      if (line) chunks[step.wordKey].push(line);
    } else {
      const text = (answers[step.id] ?? "").trim();
      if (text) chunks[step.wordKey].push(text);
    }
  });
  return {
    complaints: chunks.complaints.length ? chunks.complaints.join("\n\n") : "—",
    disease: chunks.disease.length ? chunks.disease.join("\n\n") : "—",
    life: chunks.life.length ? chunks.life.join("\n\n") : "—",
  };
}
