/**
 * @param {"male" | "female" | null} gender
 * @returns {MhComplaintBlock[]}
 */
export function getVisibleComplaintBlocks(gender) {
  return MH_COMPLAINT_BLOCKS.filter((b) => !b.femaleOnly || gender === "female");
}

/**
 * @returns {Record<string, { selected: number[]; custom: string }>}
 */
export function emptyComplaintsState() {
  /** @type {Record<string, { selected: number[]; custom: string }>} */
  const out = {};
  MH_COMPLAINT_BLOCKS.forEach((b) => {
    out[b.id] = { selected: [], custom: "" };
  });
  return out;
}

/**
 * @param {string | undefined} jsonStr
 * @returns {Record<string, { selected: number[]; custom: string }>}
 */
export function parseComplaintsString(jsonStr) {
  let raw = {};
  try {
    const p = JSON.parse(jsonStr || "{}");
    if (p && typeof p === "object") raw = p;
  } catch {
    raw = {};
  }
  const base = emptyComplaintsState();
  const blockById = Object.fromEntries(MH_COMPLAINT_BLOCKS.map((b) => [b.id, b]));
  for (const id of Object.keys(base)) {
    if (raw[id] && typeof raw[id] === "object") {
      const max = blockById[id]?.options.length ?? 0;
      const sel = Array.isArray(raw[id].selected)
        ? raw[id].selected.filter(
            (n) => typeof n === "number" && n >= 0 && n < max && Number.isInteger(n),
          )
        : [];
      base[id] = { selected: sel, custom: String(raw[id].custom ?? "") };
    }
  }
  return base;
}

/**
 * Итог по жалобам для Word и тестового блока: одна строка, только формулировки пунктов
 * (и непустой «свой вариант»), через запятую, без названий блоков и без нумерации.
 *
 * @param {Record<string, { selected: number[]; custom: string }>} state
 * @param {"male" | "female" | null} gender
 */
export function formatComplaintsForWord(state, gender) {
  /** @type {string[]} */
  const parts = [];
  const seenLower = new Set();
  /** @param {string} s */
  function addPart(s) {
    const t = s.trim();
    if (!t) return;
    const key = t.toLowerCase();
    if (seenLower.has(key)) return;
    seenLower.add(key);
    parts.push(t);
  }

  const blocks = getVisibleComplaintBlocks(gender);
  blocks.forEach((block) => {
    const row = state[block.id] ?? { selected: [], custom: "" };
    [...row.selected]
      .filter((i) => i >= 0 && i < block.options.length)
      .sort((a, b) => a - b)
      .forEach((i) => {
        const label = block.options[i];
        if (label) addPart(label);
      });
    const custom = (row.custom ?? "").trim();
    if (custom) addPart(custom);
  });

  if (parts.length === 0) return "Жалобы на: —";

  /** Первая жалоба: с заглавной буквы, остальные символы — строчные. */
  function capitalizeFirstRestLower(s) {
    const t = s.trim();
    if (!t) return t;
    const lower = t.toLocaleLowerCase("ru-RU");
    return lower.charAt(0).toLocaleUpperCase("ru-RU") + lower.slice(1);
  }

  const [first, ...rest] = parts;
  const head = capitalizeFirstRestLower(first);
  const tail = rest.map((p) => p.trim().toLocaleLowerCase("ru-RU"));
  return `Жалобы на: ${[head, ...tail].join(", ")}`;
}
