  orderedUnique
    .map((lab) => {
      const v = PAST[lab];
      const key = lab.includes('"') ? JSON.stringify(lab) : JSON.stringify(lab);
      return `  ${key}: ${JSON.stringify(v)},`;
    })
    .join("\n") +
  `\n};\n`;

