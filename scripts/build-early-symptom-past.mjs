const orderedUnique = [];
const seen = new Set();
for (const lab of labels) {
  if (seen.has(lab)) continue;
  seen.add(lab);
  orderedUnique.push(lab);
}

const keys = Object.keys(PAST);
if (keys.length !== orderedUnique.length) {
  const missing = orderedUnique.filter((l) => !PAST[l]);
  const extra = keys.filter((k) => !orderedUnique.includes(k));
  throw new Error(`Count mismatch missing=${missing.join("|")} extra=${extra.join("|")}`);
}

